
# Tipos de variavéis

# numeros inteiros ---> int
# numeros decimais ---> float
# textos ---> string
# booleanos ---> True or False



# 01 Operadores Aritméticos

# soma --->   (+)
# subtração --->   (-)
# multiplicação --->   (*)
# divisão --->   /
# potenciação --->   (**)
# Resto da divisão --->   (%)



# 02 Operadores de Atribuição
                    
# atribui o valor à variável --->   (=)
# adiciona o valor à variável e atualiza o valor da variável --->   (+=)
# subtrai o valor da variável e atualiza o valor da variável --->   (-=)
# multiplica o valor da variável e atualiza o valor da variável --->   (*=)
# divide o valor da variável e atualiza o valor da variável --->    (/=)



# 03 Operadores de Comparação

# verifica se dois valores são iguais --->   (==)
# verifica se dois valores são diferentes --->   (!=)
# verifica se o valor da esquerda é maior que o valor da direita --->   (>)
# verifica se o valor da esquerda é menor que o valor da direita --->   (<)
# verifica se o valor da esquerda é maior ou igual ao valor da direita --->   (>=)
# verifica se o valor da esquerda é menor ou igual ao valor da direita --->   (<=)



# 04 Operadores Lógicos

# retorna verdadeiro se ambas as expressões forem verdadeiras --->    and  (e)
# retorna verdadeiro se pelo menos uma das expressões for verdadeira   or  (ou)
# inverte o valor da expressão --->   not  (não)